    private void loadAccounts() {
        ViewAccountsCommand cmd = new ViewAccountsCommand();
        cmd.execute();
        List<Map<String, Object>> accounts = cmd.getResult();
        for (Map<String, Object> a : accounts) {
            Object[] row = {
                a.get("name"),
                a.get("ssn"),
                a.get("account_id"),
                a.get("Type"),
                a.get("balance"),
                a.get("status"),
                a.get("created_at"),
                
            };
            model.addRow(row);
        }

        table.setModel(model);
    }

