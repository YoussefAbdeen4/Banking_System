    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        /// change state
        
    int row = table.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(null, "Select account first", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    int accountId = (int) table.getValueAt(row, 2); 

    ChangeAccountStatusCommand cmd = new ChangeAccountStatusCommand(accountId);
    cmd.execute();

    JOptionPane.showMessageDialog(null, "Status updated successfully", "Success", JOptionPane.INFORMATION_MESSAGE);

    model.setRowCount(0);
    loadAccounts();
       
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
    /// delete 
          int row = table.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(null, "Select account first", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(
            null,
            "Are you sure you want to delete this account?",
            "Confirm",
            JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION) return;

    int accountId = (int) table.getValueAt(row, 2);

    DeleteAccountCommand cmd = new DeleteAccountCommand(accountId);
    cmd.execute();

    model.removeRow(row);

    JOptionPane.showMessageDialog(null, "Account deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton6ActionPerformed

